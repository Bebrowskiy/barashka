function translatePage(lang) {
    if (!translations[lang]) {
        console.warn('[i18n] translations not found for language:', lang);
        return;
    }
    console.log('[i18n] Translating page to:', lang);
    currentLanguage = lang;
    localStorage.setItem('app_language', lang);

    document.querySelectorAll('[data-i18n]').forEach((element) => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (
                element.tagName === 'INPUT' &&
                (element.type === 'text' || element.type === 'search') &&
                element.hasAttribute('placeholder')
            ) {
                element.placeholder = translations[lang][key];
            } else if (element.hasAttribute('data-i18n-html')) {
                // Только для элементов с data-i18n-html разрешаем HTML
                element.innerHTML = translations[lang][key];
            } else {
                element.innerText = translations[lang][key];
            }
        }
    });

    document.querySelectorAll('[data-i18n-content]').forEach((element) => {
        const key = element.getAttribute('data-i18n-content');
        if (translations[lang] && translations[lang][key]) {
            element.setAttribute('content', translations[lang][key]);
        }
    });

    document.querySelectorAll('[data-i18n-title]').forEach((element) => {
        const key = element.getAttribute('data-i18n-title');
        if (translations[lang] && translations[lang][key]) {
            element.title = translations[lang][key];
        }
    });

    // Обновляем селект языка, если он есть
    const languageSelect = document.getElementById('language-select');
    if (languageSelect) {
        languageSelect.value = lang;
    }
}

function getTranslation(key) {
    return translations[currentLanguage] && translations[currentLanguage][key]
        ? translations[currentLanguage][key]
        : translations['en'][key] || key;
}

// Try to translate immediately if DOM is already loaded
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', () => {
        console.log('[i18n] DOMContentLoaded, translating to:', currentLanguage);
        translatePage(currentLanguage);
    });
} else {
    // DOM is already loaded, translate immediately
    console.log('[i18n] DOM already loaded, translating to:', currentLanguage);
    translatePage(currentLanguage);
}

window.i18n = {
    setLanguage: translatePage,
    getLanguage: () => currentLanguage,
    t: getTranslation,
};

console.log('[i18n] Initialized with language:', currentLanguage);